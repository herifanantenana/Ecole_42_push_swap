#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t n)
{
	size_t			src_len;
	unsigned int	i;

	src_len = ft_strlen(src);
	i = 0;
	if (n <= 0)
		return (src_len);
	while (src[i] && i < n - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_len);
}
